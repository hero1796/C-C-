#include<stdio.h>
#include<math.h>
main()
{
	unsigned long n,i;
	int x=1;
	scanf("%lu",&n);
	if(n==0||n==1)
	printf("NO");
	else
	{
		if(n==2||n==3)
		printf("YES");
		else
		{
			for(i=2;i<=sqrt(n);i++)
		    {
			    if(n%i==0)
			    {
				   x=0;
			       break;
				}
		    }
		    if(x==0)
		    printf("NO");
		    else
		    printf("YES");
		}
	}
}
